
public class Voto {
	private int idVotante;
	private int voto;
	public Voto(int idVotante, int voto) {
		
		this.idVotante = idVotante;
		this.voto = voto;
	}
	public int getIdVotante() {
		return idVotante;
	}
	public void setIdVotante(int idVotante) {
		this.idVotante = idVotante;
	}
	public int getVoto() {
		return voto;
	}
	public void setVoto(int voto) {
		this.voto = voto;
	}
	
	
}
