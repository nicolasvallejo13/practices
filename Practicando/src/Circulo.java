
public class Circulo {
private int radio;
private String nombre;


public Circulo(int radio, String nombre) {
	super();
	this.radio = radio;
	this.nombre = nombre;
}
public int getRadio() {
	return radio;
}
public void setRadio(int radio) {
	this.radio = radio;
}
public String getNombre() {
	return nombre;
}
public void setNombre(String nombre) {
	this.nombre = nombre;
}

}
